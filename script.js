function clickMenu() {
    if (menu.style.display == 'block')
        menu.style.display = 'none'
    else (
        menu.style.display = 'block'
    ) 
}

function mudouTamanho() {
    if (innerWidth >= 798)
        menu.style.display = 'block'
    else {
        menu.style.display = "none"
    }
}

